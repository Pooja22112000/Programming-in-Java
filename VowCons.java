//printing if alphabet is vowel or consonant
import java.lang.*;
public class VowCons{
	public static void main(String[] args){
		char alph='A';
		
		switch(alph){
			case 'a':
			case 'e':
			case 'i':
			case 'o':
			case 'u':
			case 'A':
			case 'E':
			case 'I':
			case 'O':
			case 'U':
			System.out.println("The alphabet "+alph+" is "+"Vowel");
			break;
			default:
			System.out.println("The alphabet "+alph+" is "+"Consonant");
		}
	}
}
			
			
			
		
